                <p>插线板被广泛使用于生活工作中的每个角落，但是小小插线板在方便我们生活的同时，也给我们生活带来不少火灾隐患。我们该如何防范插线板引发的火灾呢？戳图学习，转发扩散～

                    家用电器使用广泛直接导致了插线板的普及，但是插线板的正确使用方法却经常被人们忽视。从公安部门的调查数据来看，中国2004到2017年因为插线板、开关、断路器短路等引起的火灾几乎占了这些年火灾总数的三成，因此插线板的正确使用方法不容忽视。
                </p>
                <p>使用插线板应尽量避免的误区<br>

                    1、插线板位置过低<br>

                    很多家庭在安装插线板时，觉得太高有碍美观，会装在较低的隐蔽位置。但中国科学院电工研究所夏东博士表示，这样容易在拖地时，让水溅到插线板里，从而导致漏电事故。业内规定，明装插线板距地面较好不低于1.8米；暗装插线板距地面不要低于0.3米。厨房和卫生间的插线板应距地面1.5米以上，空调的插线板至少要2米以上。
                </p>
                <p>2、插线板导线随意安装<br>

                    电源导线必须使用铜线横截面。如果住的是旧房子，一定要把原来的铝线换成铜线。因为铝线极易氧化，接头处容易打火。曾有调查显示，使用铝线的住宅，电气火灾发生率为铜线的几十倍。另外，很多家庭为了美观，会采用开槽埋线、暗管铺设的方式，夏东提醒，布线时一定要遵循“火线进开关，零线进灯头”的原则，还要在插线板上设漏电保护装置。

