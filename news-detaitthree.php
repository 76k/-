                <h1>线头这样用更安全</h1>
                <span>发表时间：2018-04-28 18:18</span>
                <b></b>
                <p>说实话，可能很多朋友都没深入关注过插座产品，甚至依然认为它们就是“接线板”，但实际情况是它们已经不再起到简单的转接功能了。由于对安全性的要求变得更高，主流插座的定位和功能都发生了变化，使用了防火性能更好的外壳材料，提供了更为完善的保护装置，能够针对浪涌、过载等情况进行更好的防御。另外还有很多产品加入了USB充电甚至是WiFi智能控制等功能。不过，作为用户和用电设备之前的一道重要安全防线，插座除了易用性，安全性问题显得更为重要，而这也是电脑报新媒体315专题重点考察的项目。
                    此次测试的12款插座售价在39元～70元，且都是各大电商平台的
                    “爆款”，如航嘉小U标准版、奥睿科、得力、飞利浦、公牛、欧普、品胜、施耐德、突破、小米、西门子、子弹头。其中10款都带有USB充电接口。
                    此次测试，我们在专业的安规实验室里进行，我们将从拔出力度测试、满载温升测试、跌落测试、耐高温测试、接地电阻测试、USB充电接口满载测试、USB高压冲击测试、750℃灼热丝燃烧测试这八个方面进行测试。
                </p>
                <p><br>从这次参与测试的插座来看，绝大部分产品还是比较靠谱的，大部分产品在大部分测试项目中都有比较不错的表现，其中跌落测试、接地电阻测试、
                    USB充电接口满载测试（仅指带USB接口的10款产品）、USB高压冲击测试四个项目全部产品均通过测试。但出乎我们意料的是，一些一线品牌的爆款插座在安全性方面做的却很差，突破和子弹头这两个品牌的插座部分项目测试甚至不过关。
                </p>
                <p>750℃灼热丝燃烧测试：在国家标准中规定，插座外壳材质必须有足够的阻燃能力。此项目要求插座外壳接触750℃灼热丝30秒后分离，插座外壳不能继续明火燃烧，且滴落的物质不能点燃仪器底部的绝缘纸。
                </p>
